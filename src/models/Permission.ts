import mongoose from "mongoose";

const { Schema } = mongoose;

const permissionSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  slug: {
    type: String,
    required: true,
    unique: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const Permission = mongoose.model("Permission", permissionSchema);

export { Permission };
